export const getRandomString = len => {
  let str = '';
  for (let i = 0; i < len; i++) {
    str += keyword[Math.floor(Math.random() * keyword.length)];
  }
  return str;
};

export const getAnonymous = () => {
  const randomName = '익명' + getRandomString(8);
  return {
    username: randomName,
    nickname: randomName,
    image: 'profile_default.png',
  };
};
